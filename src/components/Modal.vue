<template>
  <div class="modal">
    <div class="modal-content">
      <p>{{ this.modalMessage }}</p>
      <button @click="closeModal">Close</button>
    </div>
  </div>
</template>

<script>
export default {
  props: ["modalMessage"],
  methods: {
    closeModal() {
      //子元件內容要將資料傳遞到父元件，就需要使用emit這個屬性。
      this.$emit("close-modal"); //透過 this.$emit('事件名', 參數) 的方式來觸發事件。
    },
  },
};
</script>

